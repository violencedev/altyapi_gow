addEventHandler('onPlayerJoin', root, function()
    fadeCamera(source, true, 5)
    spawnPlayer(source, 0, 0, 5)
    setCameraTarget(source, source)
    setCameraMatrix(source, 1468.8785400391, -919.25317382813, 100.153465271, 1468.388671875, -918.42474365234, 99.881813049316)
    setPlayerHudComponentVisible(source,'radar', false)
    setElementData(source, 'panel', true)
    triggerEvent('call:ServerMusic', source, source)
    showChat(source, false)
end)

addCommandHandler('jp', function(pl)
    setPedWearingJetpack(pl, true)
end)